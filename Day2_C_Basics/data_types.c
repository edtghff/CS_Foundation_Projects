#include <stdio.h>

int main() {
    int age = 25;                 // Integer
    float height = 5.9;          // Floating-point
    char initial = 'A';           // Character

    printf("Age: %d\n", age);
    printf("Height: %.2f\n", height);
    printf("Initial: %c\n", initial);

    return 0;
}
